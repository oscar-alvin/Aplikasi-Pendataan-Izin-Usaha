    public void setPemilik(Data_pemilik p) {
        try {
            txt_namaPengurus.setText(p.getNama_pemilik());
            txt_tempatLahir.setText(p.getTempat_lahir());
            date_tglLahir.setDate(p.getTanggal_lahir());
            txt_alamat.setText(p.getAlamat_rumah());
            combo_prop.setSelectedItem(p.getNama_prop());
            combo_kab.setSelectedItem(p.getNama_kab());
            combo_kec.setSelectedItem(p.getNama_kec());
            combo_kel.setSelectedItem(p.getNama_kel());
            txt_noTlp.setText(p.getNo_tlp());
            txt_ktp.setText(p.getNo_ktp());
            txt_wn.setText(p.getKewarganegaraan());
        } catch (Exception e) {
            e.printStackTrace();
        }
    }

