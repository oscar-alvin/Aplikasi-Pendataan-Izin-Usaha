    private void btn_simpanActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_simpanActionPerformed
        // TODO add your handling code here:
        try {
            Data_pemilik p = new Data_pemilik();
            p.setId_pemilik(_id);
            p.setNama_pemilik(txt_namaPengurus.getText());
            p.setTempat_lahir(txt_tempatLahir.getText());
            p.setTanggal_lahir(date_tglLahir.getDate());
            p.setAlamat_rumah(txt_alamat.getText());
            p.setNama_prop(namaProp);
            p.setNama_kab(namaKab);
            p.setNama_kec(namaKec);
            p.setNama_kel(namaKel);
            p.setNo_tlp(txt_noTlp.getText());
            p.setNo_ktp(txt_ktp.getText());
            p.setKewarganegaraan(txt_wn.getText());
            
            if (new daoData_pemilik(con).updatePemilik(p) == true) {
                JOptionPane.showMessageDialog(this, "Data tersimpan");
                clear();
            }
        } catch (Exception e) {
            e.printStackTrace();
            JOptionPane.showMessageDialog(this, e.getMessage());
        }
    }//GEN-LAST:event_btn_simpanActionPerformed

