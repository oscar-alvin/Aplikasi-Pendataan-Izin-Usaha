    private void cekStatus(String jp) {
        if (jp.equalsIgnoreCase(PERUSAHAAN.CV)) {
            jLabel2.setText("Jumlah Pemegang Saham (selain yg menjabat sebagai Sekutu aktif dan Sekutu Pasif)");
        } else if (jp.equalsIgnoreCase(PERUSAHAAN.PT) || jp.equalsIgnoreCase(PERUSAHAAN.PO)) {
            jLabel2.setText("Jumlah Pemegang Saham (selain yg menjabat sebagai komisaris & direktur)");
        }
    }
