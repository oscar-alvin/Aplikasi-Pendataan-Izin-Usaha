    private Data_Saham GETSAHAM(boolean edit) {
        Data_Saham ds = null;
        try {
            ds = new Data_Saham();
            ds.setId_master(_id);
            if (edit == true) {
                ds.setId_saham(saham);
            }
            ds.setJmlPemegang_saham(Integer.parseInt(jSpinner_jumlahPsaham.getValue().toString()));
            ds.setNama_lengkap(txt_namaLengkap.getText());
            ds.setAlamat(txt_alamat.getText());
            ds.setKodePos(txt_kodePos.getText());
            ds.setNoTlp(txt_noTlp.getText());
            ds.setWn(txt_wn.getText());
            ds.setNpwp(txt_npwp.getText());
            ds.setJumlahSaham((int) jSpinner_jumlahSaham.getValue());
            ds.setJumlahSetor((Double) txt_jumlahModal.getValue());
        } catch (Exception e) {
            e.printStackTrace();
        }
        return ds;
    }

    private void SIMPAN() {
        try {
            JOptionPane.showMessageDialog(null, ts.addSaham(GETSAHAM(false)));
        } catch (SQLException | HeadlessException e) {
            e.printStackTrace();
        }
    }

    private void UPDATE() {
        try {
            JOptionPane.showMessageDialog(null, ts.updateSaham(GETSAHAM(true), row));
        } catch (SQLException | HeadlessException e) {
            e.printStackTrace();
        }
    }

    public void setSaham(Data_Saham ds) {
        try {
            this._id = ds.getId_master();
            this.saham = ds.getId_saham();
            jSpinner_jumlahPsaham.setValue(ds.getJmlPemegang_saham());
            txt_namaLengkap.setText(ds.getNama_lengkap());
            txt_alamat.setText(ds.getAlamat());
            txt_kodePos.setText(ds.getKodePos());
            txt_noTlp.setText(ds.getKodePos());
            txt_wn.setText(ds.getWn());
            txt_npwp.setText(ds.getNpwp());
            jSpinner_jumlahSaham.setValue(ds.getJumlahSaham());
            txt_jumlahModal.setValue(ds.getJumlahSetor());
        } catch (Exception e) {
            e.printStackTrace();
        }
    }

