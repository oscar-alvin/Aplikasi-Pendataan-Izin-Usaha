    public void SimpanData() {
        try {
            Data_legalitas l = new Data_legalitas();
            l.setId_legalitas(_id);
            l.setNo_akta_berdiri(txt_noBerdiri.getText());
            l.setTgl_akta_berdiri(Date_akta_berdiri.getDate());
            l.setNama_notaris_berdiri(txt_notaris1.getText());
            l.setAlamat(txt_alamat.getText());
            l.setNotlp(txt_notlp.getText());
            l.setNo_akta_perubahan1(txt_noPerubahan1.getText());
            l.setTgl_akta_perubahan1(Date_akta_perubahan1.getDate());
            l.setNama_notaris_perubahan(txt_notaris2.getText());
            l.setNo_akta_perubahan2(txt_noPerubahan2.getText());
            l.setTgl_akta_perubahan2(Date_akta_perubahan2.getDate());
            l.setNo_perse7(txt_noPersetujuan.getText());
            l.setTgl_perse7(Date_persetujuan.getDate());
            l.setNo_pengesahan(txt_noPengesahan.getText());
            l.setTglPengesahan(Date_pengesahan.getDate());
            l.setNo_laporan(txt_noLaporan.getText());
            l.setTglLaporan(Date_laporan.getDate());
            l.setNo_pemberitahuan(txt_noPemberitahuan.getText());
            l.setTglPemberitahuan(Date_Pemberitahuan.getDate());

            if (dl.updateLegalitas(l) == true) {
                clear();
                JOptionPane.showMessageDialog(this, "Data tersimpan");
            }
        } catch (SQLException | HeadlessException e) {
            e.printStackTrace();
        }
    }

