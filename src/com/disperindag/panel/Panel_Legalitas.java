    private void cekPanel(String jp, boolean b) {
        if (jp != null) {
            if (jp.equalsIgnoreCase(PERUSAHAAN.UL) || jp.equalsIgnoreCase(PERUSAHAAN.CV)) {
                pnl_akta_perubahan.setVisible(false);
                pnl_laporan.setVisible(false);
                pnl_pemberitahuan.setVisible(false);
                pnl_pengesahan.setVisible(false);
                pnl_persetujuan.setVisible(false);
            } else if (jp.equalsIgnoreCase(PERUSAHAAN.KOPERASI)) {
                pnl_laporan.setVisible(false);
                pnl_pemberitahuan.setVisible(false);
                pnl_pengesahan.setVisible(false);
                pnl_persetujuan.setBorder(
                        new TitledBorder(null, "Persetujuan Menteri Koperasi dan UKM Atas Akta Perubahan Anggaran Dasar",
                        TitledBorder.LEFT, TitledBorder.LEFT, fontBorder));
            } else if (jp.equalsIgnoreCase(PERUSAHAAN.PT)) {
                pnl_akta_perubahan.setVisible(true);
                pnl_laporan.setVisible(true);
                pnl_pemberitahuan.setVisible(true);
                pnl_pengesahan.setVisible(true);
                pnl_persetujuan.setVisible(true);
                pnl_persetujuan.setBorder(new TitledBorder(null, "Persetujuan Menteri Hukum dan HAM Atas Akta Perubahan Anggaran Dasar",
                        TitledBorder.LEFT, TitledBorder.LEFT, fontBorder));
            } else if (jp.equalsIgnoreCase(PERUSAHAAN.PO)) {
                pnl_pendirian.setVisible(false);
                pnl_akta_perubahan.setVisible(false);
                pnl_laporan.setVisible(false);
                pnl_pemberitahuan.setVisible(false);
                pnl_pengesahan.setVisible(false);
                pnl_persetujuan.setVisible(false);
            }
        }
    }

